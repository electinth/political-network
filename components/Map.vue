<script>
import * as d3 from 'd3'
import _ from 'lodash'
import { mapState, mapMutations } from 'vuex'
export default {
  props: {
    data: {
      type: Array,
      default: [],
    },
    type: {
      type: String,
      default: '',
    },
  },

  computed: {
    ...mapState(['selected_district']),
  },
  data() {
    return {
      svg: null,
    }
  },
  mounted() {
    this.svg = d3.select(`svg#${this.type}`)
    this.wrapper = d3.select(`#wrapper-${this.type}`)

    const self = this

    this.svg
      .selectAll('path')
      .attr('fill', this.type === 'surname-cluster' ? '#F15A29' : '#2BA3B4')
      .attr('class', function () {
        const district = self.data.find((d) => d.id == this.id)
        const district_name = _.get(district, 'district')
        return self.type + ' ' + district_name
      })
      .style('fill-opacity', function (d) {
        const district = self.data.find((d) => d.id == this.id)
        const district_name = _.get(district, 'district')
        const HHI_surname = _.get(district, 'HHI_surname')
        const HHI_party = _.get(district, 'HHI_party')
        return self.type === 'surname-cluster' ? HHI_surname : HHI_party
      })
      .style('cursor', 'pointer')
      .on('mouseenter', (e) => this.mouseenter(e))
      .on('mouseout', (e) => this.mouseout(e))
      .on('click', (e) => this.click(e))

    this.wrapper.append('div').attr('class', 'tooltip')

    this.svg
      .selectAll(`.${this.selected_district}`)
      .style('stroke-width', '5px')
  },
  methods: {
    ...mapMutations(['SET_DISTRICT']),
    mouseenter(e) {
      let district = _.get(e, 'target.className.baseVal')
      let className = _.split(district, ' ')
      this.wrapper
        .select(`.tooltip`)
        .style('top', e.offsetY - 10 + 'px')
        .style('left', e.offsetX + 15 + 'px')
        .style('display', 'inline')
        .text(className[1])
    },
    mouseout() {
      d3.selectAll('.tooltip').style('display', 'none')
    },
    click(e) {
      let district = _.get(e, 'target.className.baseVal')
      let className = _.split(district, ' ')
      this.SET_DISTRICT(className[1])
    },
  },
  watch: {
    selected_district(newValue, oldValue) {
      this.svg.selectAll(`.${oldValue}`).style('stroke-width', '1px')
      this.svg.selectAll(`.${newValue}`).style('stroke-width', '5px')
    },
  },
}
</script>

